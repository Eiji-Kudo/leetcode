// =============================================
// 問題: 数値の配列を受け取り、偶数だけの合計を返す関数を作れ
// for...of を使って配列を走査すること
// =============================================

function sumEvens(nums) {
  // TODO: ここに実装を書いてください
  let sum = 0;
  for (const num of nums) {
    if (num % 2 === 0) {
      sum += num
    }
  }
  return sum;
}

// --- テスト ---
console.assert(sumEvens([1, 2, 3, 4, 5, 6]) === 12, "テスト1 失敗");
console.assert(sumEvens([1, 3, 5]) === 0, "テスト2 失敗");
console.assert(sumEvens([2, 4, 6]) === 12, "テスト3 失敗");
console.assert(sumEvens([]) === 0, "テスト4 失敗");
console.assert(sumEvens([10]) === 10, "テスト5 失敗");

console.log("全テスト完了！エラーが表示されなければ正解です");
