// =============================================
// 復習問題: 隣接する要素の差の最大値
//
// 数値の配列を受け取り、隣り合う要素同士の差（絶対値）の中で
// 最大のものを返してください。
// 配列の長さが1以下の場合は 0 を返してください。
//
// 例: [3, 8, 1, 5] → |3-8|=5, |8-1|=7, |1-5|=4 → 最大は 7
// =============================================

function maxAdjacentDiff(arr) {
  // TODO: ここに実装を書いてください
  let max = 0;

  for (let i = 0; i < arr.length - 1; i++) {
    const diff = Math.abs(arr[i] - arr[i+1]);
    max = Math.max(diff, max);
  }

  return max;

}

// --- テスト ---
console.assert(maxAdjacentDiff([3, 8, 1, 5]) === 7, "テスト1 失敗");
console.assert(maxAdjacentDiff([1, 2, 3, 4]) === 1, "テスト2 失敗");
console.assert(maxAdjacentDiff([10, 1, 10, 1]) === 9, "テスト3 失敗");
console.assert(maxAdjacentDiff([5]) === 0, "テスト4 失敗");
console.assert(maxAdjacentDiff([]) === 0, "テスト5 失敗");

