// - for (let i = 0; i < arr.length; i++) の基本形
// - インデックス i で要素にアクセス (arr[i])
// - ループ内で条件分岐や累積計算を行う

// =============================================
// 復習問題: 前の要素より大きい要素を数える
//
// 数値の配列を受け取り、直前の要素より厳密に大きい要素の個数を返してください。
// （最初の要素には「直前」がないのでカウント対象外）
//
// 例: [1, 3, 2, 5, 4]
//     3 > 1 → ✓, 2 > 3 → ✗, 5 > 2 → ✓, 4 > 5 → ✗ → 2
// =============================================

function countIncreases(arr) {
  // TODO: ここに実装を書いてください
  let ans = 0;
  for (let i = 1; i < arr.length; i++) {
    if (arr[i - 1] < arr[i]) {
      ans++;
    }
  }
  return ans;
}

// --- テスト ---
console.assert(countIncreases([1, 3, 2, 5, 4]) === 2, "テスト1 失敗");
console.assert(countIncreases([5, 4, 3, 2, 1]) === 0, "テスト2 失敗");
console.assert(countIncreases([1, 2, 3, 4, 5]) === 4, "テスト3 失敗");
console.assert(countIncreases([3]) === 0, "テスト4 失敗");
console.assert(countIncreases([]) === 0, "テスト5 失敗");

console.log("全テスト完了！エラーが表示されなければ正解です");
